"""
Horner Method to find the root of polynomial function
"""

def main():
    degree = int(input("Enter the degree of polynomial:\t"))
    
    x = int(input("Enter value of x:\t"))
    
    highest_value_a = float(input(f"Enter polynomial coefficients of a{degree}:\t"))
    p = highest_value_a
    
    for i in range(degree - 1, -1, -1):
        a = float(input(f"Enter the value of a{i}:\t"))
        p = p * x + a
    
    print("\nThe functional value of given polynomial is:\t", p)

if __name__ == "__main__":
    main()
